function setup() {
    createCanvas(500, 500);
    background('#acacac');
    matrix = []
    for (let y = 0; y < 20; y++) {
        let abob = []
        for (let x = 0; x < 20; x++) {
            abob.push(Math.floor(random(0, 2)))

        }
        matrix.push(abob)
    }
    console.log(matrix)
        // frameRate(10)
}

function draw() {
    // ellipse(random(0, 500), random(0, 500), random(10, 50))


    for (var y = 0; y < matrix.length; y++) {
        for (var x = 0; x < matrix[y].length; x++) {
            if (matrix[y][x] == 1) {
                fill('black')
                rect(x * 10, y * 10, 10, 10)


            }
        }
    }

}